# OPERADORES DE COMPARACION
"""
Los operadores de comparacion, son operadores que utilizan para comparar dos valores y devolver un resultado booleano (True o False) de dicha comparacion.

Los operadores son:

1. Igualdad (==)

a == b

2. Desigualdad (!=)

a != b

3. Mayor que (>)

a > b

4. Menor que (<)

a < b

5. Mayor o igual que (>=)

a >= b

6. Menor o igual que (<=):

a <= b
"""

# IGUALDAD

"""
- Comprueba si dos valores son iguales.
- Devuelve True si son iguales, False si no lo son.

"""

x = 5
y = 5
resultado = (x == y)
print(resultado)  # Devuelve True


# DESIGUALDAD
"""
- Comprueba si dos valores no son iguales.
- Devuelve True si son diferentes, False si son iguales.
"""

a = 10
b = 20
resultado = (a != b)
print(resultado)  # Devuelve True

# MAYOR QUE
"""
- Comprueba si el valor de la izquierda es mayor que el de la derecha.
- Devuelve True si es mayor, False si no lo es.

"""

edad = 25
limite = 18
resultado = (edad > limite)
print(resultado)  # Devuelve True

# MENOR QUE
"""
- Comprueba si el valor de la izquierda es menor que el de la derecha.
- Devuelve True si es menor, False si no lo es.

"""

numero1 = 8
numero2 = 10
resultado = (numero1 < numero2)
print(resultado)  # Devuelve True

# MAYOR O IGUAL QUE
"""
- Comprueba si el valor de la izquierda es mayor o igual al de la derecha.
- Devuelve True si es mayor o igual, False si no lo es.

"""

a = 15
b = 10
resultado = (a >= b)
print(resultado)  # Devuelve True

# MENOR O IGUAL QUE
"""

- Comprueba si el valor de la izquierda es menor o igual al de la derecha.
- Devuelve True si es menor o igual, False si no lo es.

"""

numero1 = 5
numero2 = 5
resultado = (numero1 <= numero2)
print(resultado)  # Devuelve True



