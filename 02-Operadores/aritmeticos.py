# OPERADORES ARITMETICOS
"""
Los operadores aritméticos en Python se utilizan para realizar operaciones matemáticas entre variables o valores.

Los operadores aritmeticos en Python son los siguientes:

- Suma (+): operador para realizar la suma de valores

- Resta (-): operador para realizar la resta de valores

- Multiplicacion (-): operador para realizar la multiplacion de valores

- Division (/): operador para realizar la division de valores

- Modulo (%): Devuelve el residuo de la división del valor de la izquierda por el valor de la derecha
"""

num_1 = 24 # Operador de asignacion =
num_2 = 45 

print("*********** CALCULADORA ***********")

suma = num_1 + num_2
resta = num_1 - num_2
multiplacion = num_1 * num_2
division = num_1 / num_2
resto = num_1 % num_2

print(f"La suma es: {suma}")
print(f"La resta es: {resto}")
print(f"La multiplicacion es: {multiplacion}")
print(f"La division es: {division}")
print(f"El resto es: {resto}")
